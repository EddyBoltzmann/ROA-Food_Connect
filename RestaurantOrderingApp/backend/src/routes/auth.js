const express = require('express');
const jwt = require('jsonwebtoken');
const bcrypt = require('bcryptjs');
const { body, validationResult } = require('express-validator');
const User = require('../models/User');
const { auth } = require('../middleware/auth');

const router = express.Router();

// Generate JWT token
const generateToken = (userId) => {
  return jwt.sign({ userId }, process.env.JWT_SECRET, {
    expiresIn: process.env.JWT_EXPIRE || '7d'
  });
};

// Generate refresh token
const generateRefreshToken = (userId) => {
  return jwt.sign({ userId }, process.env.JWT_REFRESH_SECRET, {
    expiresIn: process.env.JWT_REFRESH_EXPIRE || '30d'
  });
};

// @route   POST /api/auth/register
// @desc    Register a new user
// @access  Public
router.post('/register', [
  body('name')
    .trim()
    .isLength({ min: 2, max: 50 })
    .withMessage('Name must be between 2 and 50 characters'),
  body('email')
    .isEmail()
    .normalizeEmail()
    .withMessage('Please enter a valid email'),
  body('password')
    .isLength({ min: 6 })
    .withMessage('Password must be at least 6 characters long'),
  body('role')
    .optional()
    .isIn(['customer', 'restaurant_owner'])
    .withMessage('Role must be either customer or restaurant_owner'),
  body('restaurantName')
    .if(body('role').equals('restaurant_owner'))
    .trim()
    .isLength({ min: 2, max: 100 })
    .withMessage('Restaurant name is required for restaurant owners'),
  body('restaurantAddress')
    .if(body('role').equals('restaurant_owner'))
    .trim()
    .isLength({ min: 10, max: 200 })
    .withMessage('Restaurant address is required for restaurant owners'),
  body('phone')
    .if(body('role').equals('restaurant_owner'))
    .matches(/^\+?[\d\s-()]+$/)
    .withMessage('Phone number is required for restaurant owners')
], async (req, res) => {
  try {
    // Check for validation errors
    const errors = validationResult(req);
    if (!errors.isEmpty()) {
      return res.status(400).json({
        success: false,
        message: 'Validation failed',
        errors: errors.array()
      });
    }

    const { 
      name, 
      email, 
      password, 
      phone, 
      role = 'customer',
      restaurantName,
      restaurantAddress
    } = req.body;

    // Check if user already exists
    const existingUser = await User.findOne({ email });
    if (existingUser) {
      return res.status(400).json({
        success: false,
        message: 'User already exists with this email'
      });
    }

    // Create user data object
    const userData = {
      name,
      email,
      password,
      role,
      phone: phone || undefined
    };

    // If restaurant owner, add restaurant-specific fields
    if (role === 'restaurant_owner') {
      userData.restaurantName = restaurantName;
      userData.restaurantAddress = restaurantAddress;
    }

    // Create new user
    const user = new User(userData);

    await user.save();

    // Generate tokens
    const token = generateToken(user._id);
    const refreshToken = generateRefreshToken(user._id);

    // Remove password from response
    user.password = undefined;

    res.status(201).json({
      success: true,
      message: 'User registered successfully',
      data: {
        user,
        token,
        refreshToken
      }
    });
  } catch (error) {
    console.error('Registration error:', error);
    res.status(500).json({
      success: false,
      message: 'Server error during registration'
    });
  }
});

